using System;
using System.IO;

namespace PdfBox.Net.IO;

/// <summary>
/// A stream which writes to a <see cref="RandomAccessWrite"/>.
/// </summary>
public class RandomAccessOutputStream : Stream
{
    private readonly RandomAccessWrite _writer;

    /// <summary>
    /// Constructor to create a new output stream which writes to the given RandomAccessWrite.
    /// </summary>
    /// <param name="writer">The random access writer for output</param>
    public RandomAccessOutputStream(RandomAccessWrite writer)
    {
        _writer = writer;
    }

    public override bool CanRead => false;

    public override bool CanSeek => false;

    public override bool CanWrite => true;

    public override long Length => throw new NotSupportedException();

    public override long Position
    {
        get => throw new NotSupportedException();
        set => throw new NotSupportedException();
    }

    public override void Flush()
    {
    }

    public override int Read(byte[] buffer, int offset, int count)
    {
        throw new NotSupportedException();
    }

    public override long Seek(long offset, SeekOrigin origin)
    {
        throw new NotSupportedException();
    }

    public override void SetLength(long value)
    {
        throw new NotSupportedException();
    }

    public override void Write(byte[] buffer, int offset, int count)
    {
        _writer.Write(buffer, offset, count);
    }

    public override void WriteByte(byte value)
    {
        _writer.Write(value);
    }
}
